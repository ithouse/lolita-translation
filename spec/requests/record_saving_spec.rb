require 'spec_helper'

describe "In order to internationalize all content I can enter information in any language and translate to any other language" do 
  it "As user I can enter information in same language as system uses and record will be stored in that language" 

  it "As user I can enter information in language that I am using currently and original record will be save in that language"

  it "As user I can open previously saved record, in different language than mine, and record will be shown in translation view for my language"

  it "As user I can open previously saved record, in different language than mine, and change original information and that information will be save in original record"

  it "As user I can open previously saved record, in different language than mine, and change information for my language, and it will be saved as translation"
end